
public abstract class Towers {
	protected int cost;
	protected int range;
	protected int damage;
	protected int posX;
	protected int posY;
	protected int rateOfFire;
	protected String name;
	
	public int getDmg() {
		return damage;
	}
	
	public int getPosY() {
		return posY;
	}
	
	public int getPosX() {
		return posX;
	}
}
